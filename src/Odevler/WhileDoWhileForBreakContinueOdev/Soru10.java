package Odevler.WhileDoWhileForBreakContinueOdev;

public class Soru10 {
    public static void main(String[] args) {
        //  0 ile 30 arasındaki bütün çift sayıları yazdırınız.

        int sayi=0;
        do {
            System.out.println(sayi);
            sayi=sayi+2; }

        while (sayi<30);
    }
}
